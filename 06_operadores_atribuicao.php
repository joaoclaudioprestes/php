<?php

// = -> define a value to a variable
// += -> sum the value of the variable with the value
// -= -> subtract the value of the variable with the value
// *= -> multiply the value of the variable with the value
// /= -> divide the value of the variable with the value
// %= -> get the modulus of the value of the variable with the value
// .= -> concatenate the value of the variable with the value
// . -> concatenate two strings

$number = 10;
$number += 5;
$number -= 5;
$number *= 5;
$number /= 5;
$number %= 5;
$number .= 5;