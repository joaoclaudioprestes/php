<?php

// switch - used to make decisions

$color = "red";

switch ($color) {
  case "red":
    echo "The color is red";
    break;
  case "blue":
    echo "The color is blue";
    break;
  case "green":
    echo "The color is green";
    break;
  default:
    echo "The color is not red, blue or green";
    break;
}
